package com.zx.erpsystem.system.model;

public class CommodityManagement {
    private Integer commodityManagementNo;

    private String commodityName;

    private String commodityOriginAddress;

    private String commodityUnit;

    private String commoditySpecification;

    private String commodityBatchNumber;

    public Integer getCommodityManagementNo() {
        return commodityManagementNo;
    }

    public void setCommodityManagementNo(Integer commodityManagementNo) {
        this.commodityManagementNo = commodityManagementNo;
    }

    public String getCommodityName() {
        return commodityName;
    }

    public void setCommodityName(String commodityName) {
        this.commodityName = commodityName;
    }

    public String getCommodityOriginAddress() {
        return commodityOriginAddress;
    }

    public void setCommodityOriginAddress(String commodityOriginAddress) {
        this.commodityOriginAddress = commodityOriginAddress;
    }

    public String getCommodityUnit() {
        return commodityUnit;
    }

    public void setCommodityUnit(String commodityUnit) {
        this.commodityUnit = commodityUnit;
    }

    public String getCommoditySpecification() {
        return commoditySpecification;
    }

    public void setCommoditySpecification(String commoditySpecification) {
        this.commoditySpecification = commoditySpecification;
    }

    public String getCommodityBatchNumber() {
        return commodityBatchNumber;
    }

    public void setCommodityBatchNumber(String commodityBatchNumber) {
        this.commodityBatchNumber = commodityBatchNumber;
    }
}